class TLicitacaoTipoChamadaPublica extends Persistente
{
/**
    * Método Construtor
    * @access Private
*/
    function TLicitacaoTipoChamadaPublica()
    {
        parent::Persistente();
        $this->setTabela("licitacao.tipo_chamada_publica");
    
        $this->setCampoCod('cod_tipo');
        $this->setComplementoChave('descricao');
    
        $this->AddCampo('cod_tipo' ,'integer',true ,''  ,true ,true);
        $this->AddCampo('descricao','varchar',true,'50' ,false,false);
    }

}

