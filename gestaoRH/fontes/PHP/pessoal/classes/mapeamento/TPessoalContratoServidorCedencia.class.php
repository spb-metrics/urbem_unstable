class TPessoalContratoServidorCedencia extends Persistente
{
/**
    * Método Construtor
    * @access Private
*/
function TPessoalContratoServidorCedencia()
{
    parent::Persistente();
    $this->setTabela('pessoal.contrato_servidor_cedencia');

    $this->setCampoCod('cod_contrato');
    $this->setComplementoChave('');

    $this->AddCampo('cod_contrato','integer',true,'',true,true);
    $this->AddCampo('cod_tipo','integer',true,'',false,true);

}

/**
    * Executa um Select no banco de dados a partir do comando SQL montado no método montaRecuperaRelacionamento
    * @access Public
    * @param  Object  $rsRecordSet Objeto RecordSet
    * @param  String  $stFiltro    String de Filtro do SQL (WHERE)
    * @param  String  $stOrdem     String de Ordenação do SQL (ORDER BY)
    * @param  Boolean $boTransacao
    * @return Object  Objeto Erro
*/
function recuperaRelacionamento(&$rsRecordSet, $stFiltro = "", $stOrdem = "" , $boTransacao = "")
{
    $obErro      = new Erro;
    $obConexao   = new Conexao;
    $rsRecordSet = new RecordSet;
    $stSql = $this->montaRecuperaRelacionamento().$stFiltro.$stOrdem;
    $this->setDebug( $stSql );
    $obErro = $obConexao->executaSQL( $rsRecordSet, $stSql, $boTransacao );

    return $obErro;
}

function montaRecuperaRelacionamento()
{
    $stSql .= "SELECT cont_cedencia.*                                       \n";
    $stSql .= "     , cedencia.descricao                                    \n";
    $stSql .= "  FROM pessoal.contrato_servidor_cedencia as cont_cedencia   \n";
    $stSql .= "     , pessoal.tipo_cedencia as cedencia                     \n";
    $stSql .= " WHERE cont_cedencia.cod_tipo = cedencia.cod_tipo            \n";

    return $stSql;
}

}
