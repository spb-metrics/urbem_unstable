/**
  * Efetua conexão com a tabela  PESSOAL.CONTRATO_SERVIDOR_CEDENCIA
  * Data de Criação: 08/09/2005

  * @author Analista: Vandré Miguel Ramos
  * @author Desenvolvedor: Diego Lemos de Souza

