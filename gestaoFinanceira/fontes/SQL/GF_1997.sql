UPDATE administracao.configuracao
   SET valor = 'false'
 WHERE parametro =  'seta_tipo_documento_liq_tceam';

UPDATE administracao.configuracao
   SET valor = 'true'
 WHERE parametro =  'seta_tipo_documento_liq_tceam'
   AND EXISTS (SELECT cod_uf 
                 FROM sw_uf 
                WHERE cod_uf IN (SELECT cod_uf 
                                   FROM sw_cgm 
                                  WHERE nom_cgm ILIKE '%prefeitura municipal%') 
                  AND sigla_uf = 'AM');


