CREATE OR REPLACE FUNCTION manutencao() RETURNS VOID AS $$

DECLARE

   varAux     VARCHAR;

BEGIN

   SELECT valor
     INTO varAux
     FROM administracao.configuracao
    WHERE exercicio = '2011'
      AND parametro = 'cnpj'
      AND valor     = '08148553000106';

   IF FOUND THEN

        UPDATE arrecadacao.modelo_carne SET nom_arquivo = 'RCarneIPTUItau.class.php', nom_modelo = 'Carnê IPTU' WHERE cod_modelo = 2;

   END IF;

END;

$$ LANGUAGE 'plpgsql';
SELECT manutencao();
DROP FUNCTION manutencao();

